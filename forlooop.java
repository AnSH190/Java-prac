public class forlooop {
    public static void main(String[] args) {
        
        
        for(int i =1; i<=10; i++){
            System.out.println(i);
        }

        System.out.println("---------------------------------------");
        System.out.println("---------------------------------------");
        System.out.println("---------------------------------------");
        

        for(int j=10; j>=1; j-=2) {
            System.out.println(j);
        }

        System.out.println("Happy new year  ");
    }
}
